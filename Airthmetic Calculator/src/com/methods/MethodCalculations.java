package com.methods;

public class MethodCalculations {

	//Method Add()
	protected float Add(float a, float b) {
		return a+b;
	}
	//Method Subtract()
	protected float Subtract(float a, float b) {
		return a-b;
	}
	//Method Multiply()
	protected float Multiply(float a, float b) {
		return a*b;
	}
	//Method Divide()
	protected float Divide(float a, float b) {
		return a/b;
	}

}
